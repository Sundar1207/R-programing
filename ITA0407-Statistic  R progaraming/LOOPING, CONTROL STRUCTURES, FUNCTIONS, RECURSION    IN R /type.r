# Create vectors
numeric_data <- c(1, 2, 3, 4, 5)
character_data <- c("apple", "banana", "cherry", "date")
logical_data <- c(TRUE, FALSE, TRUE, FALSE)

# Display the vectors and their type
print("Numeric Data:")
print(numeric_data)
print(paste("Type:", class(numeric_data)))

print("Character Data:")
print(character_data)
print(paste("Type:", class(character_data)))

print("Logical Data:")
print(logical_data)
print(paste("Type:", class(logical_data)))
